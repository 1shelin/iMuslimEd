def export_to_json():
    """–≠–∫—Å–ø–æ—Ä—Ç–∏—Ä—É–µ—Ç –±–∞–∑—É –¥–∞–Ω–Ω—ã—Ö –≤ JSON-—Ñ–æ—Ä–º–∞—Ç"""
    
    conn = sqlite3.connect(DB_NAME)
    cursor = conn.cursor()
    
    # –ü–æ–ª—É—á–∞–µ–º –≤—Å–µ –±–ª–æ–∫–∏ —Å –≤–æ–ø—Ä–æ—Å–∞–º–∏
    data = {}
    
    cursor.execute("SELECT id, name, description FROM blocks")
    blocks = cursor.fetchall()
    
    for block_id, block_name, description in blocks:
        cursor.execute('''
            SELECT question_number, question, answer
            FROM qa_pairs
            WHERE block_id = ?
            ORDER BY question_number
        ''', (block_id,))
        
        questions = []
        for q_num, q, a in cursor.fetchall():
            questions.append({
                "number": q_num,
                "question": q,
                "answer": a
            })
        
        data[block_name] = {
            "description": description,
            "questions": questions
        }
    
    conn.close()
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ JSON —Ñ–∞–π–ª
    with open("imuslimed_export.json", "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f"\nüìÅ –î–∞–Ω–Ω—ã–µ —ç–∫—Å–ø–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω—ã –≤ —Ñ–∞–π–ª imuslimed_export.json")

def search_questions(keyword):
    """–ü–æ–∏—Å–∫ –≤–æ–ø—Ä–æ—Å–æ–≤ –ø–æ –∫–ª—é—á–µ–≤–æ–º—É —Å–ª–æ–≤—É"""
    conn = sqlite3.connect(DB_NAME)
    cursor = conn.cursor()
    
    cursor.execute('''
        SELECT b.name, q.question_number, q.question, q.answer
        FROM qa_pairs q
        JOIN blocks b ON q.block_id = b.id
        WHERE q.question LIKE ? OR q.answer LIKE ?
        ORDER BY b.id, q.question_number
    ''', (f'%{keyword}%', f'%{keyword}%'))
    
    results = cursor.fetchall()
    conn.close()
    
    return results

